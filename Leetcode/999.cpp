#include<vector>
#include<string>

int main()
{
	std::vector<std::vector<std::string>> board_2 ={ {".",".",".",".",".",".",".","."},{".",".",".","p",".",".",".","."},{".",".",".","R",".",".",".","p"},{".",".",".",".",".",".",".","."},{".",".",".",".",".",".",".","."},{".",".",".","p",".",".",".","."},{".",".",".",".",".",".",".","."},{".",".",".",".",".",".",".","." } };
	std::vector<std::vector<std::string>> board_3 = { {".", ".", ".", ".", ".", ".", ".", "."},{".", ".", ".", "p", ".", ".", ".", "."},{".", ".", ".", "p", ".", ".", ".", "."},{"p", "p", ".", "R", ".", "p", "B", "."},{".", ".", ".", ".", ".", ".", ".", "."},{".", ".", ".", "B", ".", ".", ".", "."},{".", ".", ".", "p", ".", ".", ".", "."},{".", ".", ".", ".", ".", ".", ".", "."} };
	std::vector<std::vector<std::string>> board = { {".", ".", ".", ".", "B", ".", ".", "."},{".", ".", ".", ".", "p", ".", ".", "."},{".", ".", ".", ".", ".", ".", ".", "."},{".", "B", "p", ".", "R", ".", "p", "B"},{".", ".", ".", ".", ".", ".", ".", "."},{".", ".", ".", ".", "p", ".", ".", "."},{".", ".", ".", ".", "B", ".", ".", "."},{".", ".", ".", ".", ".", ".", ".", "."} };
	int z = 0, y = 0, u = 0, d = 0;
	int col = 0, row = 0;
	for (int i = 0; i<8; ++i)
	{
		for (int j = 0; j<8; ++j) {
			if (board[i][j] == "R")
			{
				col = j, row = i;
			}
		}
	}
	//left
	for (int i = 0; i<col; i++)
	{
		if (board[row][i] == "p") {
			z = 1;
		}
		if (board[row][i] == "B") {
			z = 0;
		}
	}

	//right
	for (int i = col+1; i<8; i++)
	{
		if (board[row][i] == "p") {
			y = 1; 
			break;
		}
		if (board[row][i] == "B") { y = 0; break; }
	}

	//up
	for (int i = 0; i<row; i++)
	{
		if (board[i][col] == "p") { u = 1; }
		if (board[i][col] == "B") { u = 0; }
	}

	//down
	for (int i = row+1; i<8; i++)
	{
		if (board[i][col] == "p") { d = 1; break; }
		else if (board[i][col] == "B") { d = 0; break; }
	}
	int ans = z + y + u + d;
	return ans;
}